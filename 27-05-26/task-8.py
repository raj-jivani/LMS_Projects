'''
Create a list of 5 fruits. Print the second and last fruit.
- Add "Mango" to the list. Remove the first element.
- Sort the list alphabatically. reverse it.
'''

fruits = ["banana", "apple", "orange", "grapes"]

fruits.append("mango")
print(fruits)

